nilai_pertama = float(input("Masukkan Nilai Pertama: "))
nilai_kedua = float(input("Masukkan Nilai Kedua: "))

hasil = nilai_pertama + nilai_kedua

output = "Hasil dari penjumlahan nilai pertama {:.2f} dan nilai kedua {:.2f} adalah {:.2f}".format(nilai_pertama, nilai_kedua, hasil)
print(output)
